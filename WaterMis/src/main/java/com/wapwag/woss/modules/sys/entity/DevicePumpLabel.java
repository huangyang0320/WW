package com.wapwag.woss.modules.sys.entity;

public class DevicePumpLabel {
    private String id;

    private String deviceId;

    private String modelNo;

    private String sbedgsll;

    private String ratedPower;

    private String sbedgsyc;

    private String speed;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDeviceId() {
        return deviceId;
    }

    public void setDeviceId(String deviceId) {
        this.deviceId = deviceId;
    }

    public String getModelNo() {
        return modelNo;
    }

    public void setModelNo(String modelNo) {
        this.modelNo = modelNo;
    }

    public String getSbedgsll() {
        return sbedgsll;
    }

    public void setSbedgsll(String sbedgsll) {
        this.sbedgsll = sbedgsll;
    }

    public String getRatedPower() {
        return ratedPower;
    }

    public void setRatedPower(String ratedPower) {
        this.ratedPower = ratedPower;
    }

    public String getSbedgsyc() {
        return sbedgsyc;
    }

    public void setSbedgsyc(String sbedgsyc) {
        this.sbedgsyc = sbedgsyc;
    }

    public String getSpeed() {
        return speed;
    }

    public void setSpeed(String speed) {
        this.speed = speed;
    }
}